package com.BestofallHealthYoga.TamarinYoga.models;

public class ChartData {
    double measvalue;
    String timemille;

    public double getMeasvalue() {
        return measvalue;
    }

    public void setMeasvalue(double measvalue) {
        this.measvalue = measvalue;
    }

    public String getTimemille() {
        return timemille;
    }

    public void setTimemille(String timemille) {
        this.timemille = timemille;
    }

    public double getMeasurementWeight() {
        return this.measvalue;
    }
}
